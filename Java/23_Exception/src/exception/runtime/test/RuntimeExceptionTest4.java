package exception.runtime.test;
/*
 * 예외를 처리하는 방법 2번째...throws사용해본다.
 */
class Throws{
	//go() 수행하던중 예외가 발생...그걸 던지니...go()를 호출한 곳에서 예외처리를 직접 하세요.
	public void go() throws ArithmeticException{//4. //9.
		int i = 10;//5.
		int j = 0;//6.
		
		System.out.println("2. 0을 분모로 하면 안됩니다....");//7. print 하고
		if(i/j==0) //8.예외가 발생 해서 throws
			System.out.println("ArithmeticException....Boom....");
		
	}
}
public class RuntimeExceptionTest4 {

	public static void main(String[] args) {
		
		Throws t = new Throws();//1. 이 때는 예외 감지 못함 (compile만하고 실행은 안하기 때문)
		System.out.println("1. go() calling....");//2. print 하고
		try{//10.
			t.go();//3. go worker method로 간다
		}catch(ArithmeticException e) {//11.
			System.out.println("3. 호출한 지점에서 예외를 잡았습니다.");//12. print
		}
		

	}

}
